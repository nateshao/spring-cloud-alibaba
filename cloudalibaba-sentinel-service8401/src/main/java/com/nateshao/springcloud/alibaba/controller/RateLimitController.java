package com.nateshao.springcloud.alibaba.controller;

import com.alibaba.csp.sentinel.annotation.SentinelResource;
import com.alibaba.csp.sentinel.slots.block.BlockException;
import com.nateshao.springcloud.alibaba.handler.CustomerBlockHandler;
import com.nateshao.springcloud.entities.CommonResult;
import com.nateshao.springcloud.entities.Payment;
